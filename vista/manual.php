<div class="row justify-content-center">
  <div class="col-lg-10 col-xl-9">
    <h3 class="mt-3 mb-3">Manual para estudiantes</h3>

    <p>Bienvenido. Aquí encontrarás, en pasos simples, cómo registrarte, iniciar sesión y evaluar a tus profesores.</p>

    <h4>Antes de empezar</h4>
    <ul>
      <li>Ten a la mano tu <strong>matrícula</strong>, tu <strong>cuatrimestre</strong> y tu <strong>grupo</strong>.</li>
      <li>Si es tu primera vez, primero crea tu cuenta.</li>
    </ul>

    <hr />
    <h4>Registro de alumno</h4>
    <p>Para crear tu cuenta:</p>
    <ul>
      <li>En el menú, entra a <strong>Registrar</strong> o ve a la pantalla de <strong>Login</strong> y usa el enlace <strong>Crear cuenta</strong>.</li>
      <li>Escribe tu <strong>matrícula</strong> y tu <strong>nombre</strong>.</li>
      <li>Selecciona tu <strong>carrera</strong>.</li>
      <li>Indica tu <strong>cuatrimestre</strong> (por ejemplo, “4to”).</li>
      <li>Indica tu <strong>grupo</strong> (por ejemplo, “A”).</li>
      <li>Elige una <strong>contraseña</strong> y confírmala.</li>
      <li>Presiona <strong>Crear cuenta</strong>.</li>
    </ul>
    <p>Si algo falta o tu matrícula ya existe, verás un mensaje explicando el problema. Si todo está bien, te avisará que la cuenta se creó y podrás iniciar sesión.</p>

    <hr />
    <h4>Inicio de sesión</h4>
    <p>Para entrar con tu cuenta:</p>
    <ul>
      <li>Ve a <strong>Login</strong>.</li>
      <li>Escribe tu <strong>matrícula</strong> y tu <strong>contraseña</strong>.</li>
      <li>Indica tu <strong>cuatrimestre</strong> y tu <strong>grupo</strong> actuales.</li>
      <li>Presiona <strong>Acceder y evaluar</strong>.</li>
    </ul>
    <p>Si la matrícula o la contraseña no son correctas, te lo diremos para que lo revises. Si falta cuatrimestre o grupo, te pediremos que los ingreses. Si el cuatrimestre o el grupo no coinciden con tu perfil, actualízalos primero en tu <strong>Perfil</strong>.</p>

    <hr />
    <h4>Perfil del alumno</h4>
    <p>Para acceder a tu perfil, haz clic en tu nombre (arriba a la derecha). Ahí verás tu <strong>Nombre</strong>, <strong>Carrera</strong>, <strong>Grado (grupo)</strong> y podrás actualizar tu <strong>Cuatrimestre</strong>.</p>
    <ul>
      <li>El <strong>Cuatrimestre</strong> se puede actualizar cuando cambias de ciclo.</li>
      <li>Para iniciar sesión, el cuatrimestre y el grupo deben coincidir con tu perfil.</li>
    </ul>

    <hr />
    <h4>Evaluación de profesores</h4>
    <p>Una vez dentro, podrás evaluar a tus profesores:</p>
    <ul>
      <li>Verás la lista de docentes asociados a tu carrera/cuatrimestre/grupo.</li>
      <li>Para cada docente, responde las preguntas marcando una calificación del <strong>1</strong> al <strong>5</strong>.</li>
      <li>Puedes evaluar a uno o a varios. Asegúrate de marcar al menos alguna respuesta.</li>
      <li>Al finalizar, presiona <strong>Enviar</strong> para guardar tus respuestas.</li>
    </ul>
    <p>Si no has respondido nada, te avisaremos para que completes al menos una pregunta. Cuando se guarden tus respuestas, verás un mensaje de confirmación.</p>

    <p class="text-muted">Nota: Cada alumno puede evaluar a un docente una sola vez por <strong>cuatrimestre</strong> y <strong>grupo</strong>.</p>

    <hr />
    <h4>Estado de evaluación (DPA)</h4>
    <p>La evaluación puede estar <strong>activada</strong> o <strong>desactivada</strong> por DPA. Si está desactivada, no podrás evaluar hasta que se active de nuevo. Al desactivar, se reinician los promedios y comentarios para empezar el ciclo desde cero.</p>

    <hr />
    <h4>Consejos rápidos</h4>
    <ul>
      <li>Escribe tu matrícula sin espacios ni símbolos.</li>
      <li>Elige una contraseña que recuerdes y no compartas.</li>
      <li>Si algo falla, actualiza la página y vuelve a intentarlo.</li>
    </ul>

    <p class="text-muted">Nota: los accesos de Docente y DPA están pensados para personal académico y administración. Como estudiante, tu flujo es Registro → Login → Evaluación.</p>
  </div>
</div>
